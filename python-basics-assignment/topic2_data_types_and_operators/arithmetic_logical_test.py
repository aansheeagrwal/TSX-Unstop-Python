
# Testing arithmetic and logical operations

a = 10
b = 3

# Arithmetic operations
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)

# Logical operations
x = True
y = False

print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

